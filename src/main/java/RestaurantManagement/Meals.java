package RestaurantManagement;



public class Meals {
    private int id;
    private String cost;
    private String type;
    private String name;

    public Meals(int id, String cost, String type, String name) {
        this.id = id;
        this.cost = cost;
        this.type = type;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
